#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	index;

	index = 0;
	if (!s)
		return ;
	while (s[index])
	{
		(*f)(index, &s[index]);
		index++;
	}
}
